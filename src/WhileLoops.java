public class WhileLoops {
    public static void main(String[] args) {
        int value = 0;                        // Заданое изначальное число
        while(value<5){                      // Цикл while выполняет действие до опред. момента 0<5
            System.out.println("Accept");    // Вывод сообщения в случае выполненого цикла
            value = value+1;                 // Функция увиличения заданного числа 0 каждый цикл +1
        }
    }
}
