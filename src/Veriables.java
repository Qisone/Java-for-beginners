public class Veriables {
    public static void main (String [] args) {
        int myInt = 557;                   // Вмещает 32 бита число (целочисленные типы данных)
        short myShort = 3266;              // Вмещает 16 бит число (целочисленные типы данных)
        long myLong = 2456765;             // Вмещает 64 бита число (целочисленные типы данных)

        double myDouble = 223.44;          // Хранит 64 бита информации (вещественные типы данных)
        float myFloat = 2345.4f;           // Вмещает 32 бита информации (вещественные типы данных)

        char c = 'a';                       // Символ-буква
        boolean b = true;                   // Принимает либо "true" либо "false"

        byte myByte = 100;                  // Говорит само за себя, хранит 1 байт инфо. числа от -128 до 128
        System.out.println(myInt);
    }
}
